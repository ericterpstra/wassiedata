function getReversedBins(wassieType, traitName) {
  if (!bins[wassieType]?.[traitName]) {
    return;
  }
  let trait = bins[wassieType][traitName];
  let invertedObj = {};

  for (const bin in trait) {
    trait[bin].forEach((traitValue) => {
      invertedObj[traitValue] = bin;
    });
  }
  return invertedObj;
}
module.exports = { bins, getReversedBins };
